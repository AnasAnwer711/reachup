    {
        try {
            $data['reportings'] = Report::whereNull('parent_id')->select('id', 'title', 'parent_id')->with(array('sub_reportings'=>function($query) {
                $query->select('id','title', 'parent_id');
            }))->get();
            
            return $this->sendResponse($data, 'Reporting retrieved successfully.');
        } catch (\Throwable $th) {
            if(\App::environment() == 'production'){
                return $this->sendError('Error Occured');
            } else {
                return $this->sendError('Error Occured', $th->getMessage());
            }  
        }
    }

